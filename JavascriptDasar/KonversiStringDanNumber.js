// parseInt() : mengubah string menjadi number
const score = '100';
const parsed = parseInt(score);

console.log(typeof score);
console.log(typeof parsed);

console.log(score + 2);
console.log(parsed + 2);

const nilai = 'tes';
console.log(parseInt(nilai)); //NaN

